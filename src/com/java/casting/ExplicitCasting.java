package com.java.casting;

public class ExplicitCasting {

	public static void main(String[] args) {
		float a = 66.77f;
		long b = (long) a;
		System.out.println("value of b is :" +a);
		
	    long q = 987654321l;
	    int w = (int) q;
	    System.out.println("value of w is :" +q);
	    
	    
	    long e = 9876541230l;
	    short r = (short) e;
	    System.out.println("value of r is :" +e);

	}

}
